function getCheckedColor() {
    let checkItem = document.querySelectorAll('input[type = "checkbox"]')

    let colorCollection = [];

    checkItem.forEach((element, index) => {
        if (element.checked === true) {
            colorCollection.push(element.name)
            // console.log(colorCollection)
        }
    })

    if (colorCollection.value !== 0) {
        filterObj.color = colorCollection

    } else {
        filterObj.color = null
    }
}






